package com.centuryweb.centuryweb.logica;

public class Usuario {

private String name;
private String id;
private String age;
private String email;
private String password;

    public Usuario() {
    }

    public Usuario(String name, String id, String age, String email, String password) {
        this.name = name;
        this.id = id;
        this.age = age;
        this.email = email;
        this.password = password;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
    
    }
    


